package com.xworkz.granite.granites;

public class Granite {
    private int graniteId;
    private String graniteBrand;
    private String graniteType;
    private double graniteThickness;
    private double granitePrice;
    private String graniteColor;
    private String graniteOrigin;

    public void setGraniteId(int graniteId) {
        this.graniteId = graniteId;
    }
    public int getGraniteId() {
        return graniteId;
    }

    public void setGraniteBrand(String graniteBrand) {
        this.graniteBrand = graniteBrand;
    }
    public String getGraniteBrand() {
        return graniteBrand;
    }

    public void setGraniteType(String graniteType) {
        this.graniteType = graniteType;
    }
    public String getGraniteType() {
        return graniteType;
    }

    public void setGraniteThickness(double graniteThickness) {
        this.graniteThickness = graniteThickness;
    }
    public double getGraniteThickness() {
        return graniteThickness;
    }

    public void setGranitePrice(double granitePrice) {
        this.granitePrice = granitePrice;
    }
    public double getGranitePrice() {
        return granitePrice;
    }

    public void setGraniteColor(String graniteColor) {
        this.graniteColor = graniteColor;
    }
    public String getGraniteColor() {
        return graniteColor;
    }

    public void setGraniteOrigin(String graniteOrigin) {
        this.graniteOrigin = graniteOrigin;
    }
    public String getGraniteOrigin() {
        return graniteOrigin;
    }
}