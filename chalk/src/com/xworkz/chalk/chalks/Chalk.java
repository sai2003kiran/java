package com.xworkz.chalk.chalks;

public class Chalk {
    private String chalkColor;
    private String chalkMaterial;
    private int chalkLength;
    private boolean isDustless;
    private double chalkWeight;
    private String chalkUsage;
    private String chalkBrand;

    public void setChalkColor(String chalkColor) {
        this.chalkColor = chalkColor;
    }

    public String getChalkColor() {
        return chalkColor;
    }

    public void setChalkMaterial(String chalkMaterial) {
        this.chalkMaterial = chalkMaterial;
    }

    public String getChalkMaterial() {
        return chalkMaterial;
    }

    public void setChalkLength(int chalkLength) {
        this.chalkLength = chalkLength;
    }

    public int getChalkLength() {
        return chalkLength;
    }

    public void setDustless(boolean isDustless) {
        this.isDustless = isDustless;
    }

    public boolean getDustless() {
        return isDustless;
    }

    public void setChalkWeight(double chalkWeight) {
        this.chalkWeight = chalkWeight;
    }

    public double getChalkWeight() {
        return chalkWeight;
    }

    public void setChalkUsage(String chalkUsage) {
        this.chalkUsage = chalkUsage;
    }

    public String getChalkUsage() {
        return chalkUsage;
    }

    public void setChalkBrand(String chalkBrand) {
        this.chalkBrand = chalkBrand;
    }

    public String getChalkBrand() {
        return chalkBrand;
    }

}
